package com.mycompany.potencias;

public class CalcularPotencias {
   public static Double Cuadrado(double A){
       return Math.pow(A,2);
   } 
   public static Double Cubo(double A){
       return Math.pow(A,3);
   }
   public static Double Sqrt(double A){
       return Math.sqrt(A);
   }

}
