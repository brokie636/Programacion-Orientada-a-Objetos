    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        String nombre;
        double SalarioHora, horas,SalarioMes;
        nombre = txtNombre.getText();
        SalarioHora = Double.parseDouble(txtSalarioHora.getText());
        horas = Double.parseDouble(txtHoras.getText());
        SalarioMes = CalcularSalario.Salario(SalarioHora, horas);
        if (SalarioMes>450000){
            txtNombreEmpleado.setText(nombre);
            txtSalarioMensual.setText((String.valueOf(String.format("%.2f",CalcularSalario.Salario(SalarioHora, horas)))));
        } else{
            txtNombreEmpleado.setText(nombre);
            txtSalarioMensual.setText("");
        }
    }//GEN-LAST:event_btnValidarActionPerformed

